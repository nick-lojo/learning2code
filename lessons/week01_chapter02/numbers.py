#Integer operations

print(2 + 3)
print(3 - 2)
print(2 * 3)
print(3 / 2)
print(3 ** 2) #Exponent represented by **
print(2 + 3 * 4) #Order of operations
print((2+3) * 4) #Parenthases change order

#Floats - numbers with decimal points

print(0.1 + 0.1)
print(0.2 + 0.2)
print(2 * 0.1)

#Division always returns a float

print(4/2)

#Mixing integers and floats returns floats

print(1 + 2.0)
print(2 * 3.0)

#Underscores make large numbers more visible

universe_age = 14_000_000_000
print(universe_age)

#Multiple assignments in one line

x, y, z = 0, 0, 0
print(x)
print(y)
print(z)

#Constants use all capital letters

MAX_CONNECTIONS = 5000
print(MAX_CONNECTIONS)

